# basics - data types 
# working in command line
# julia> is the prompt
# version: 1.0

# numero uno
julia> 1
1

# numero dos
julia> 2
2

# float
julia> 2.3
2.3

# typeof
julia> typeof(1)
Int64

julia> typeof(2.3)
Float64

# Julia Sys
julia> Sys
Base.Sys

# Julia Sys Length
julia> Sys.length(1)
1

# Julia String declaration
julia> a = "praveen"
"praveen"

julia> a
"praveen"

julia> Sys.length(a)
7

