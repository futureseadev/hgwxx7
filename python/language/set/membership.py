# use case for set

# input list
my_list = ['random','range','python','freedom','open','dreams','live','unknown','a',
            'the','is','you','are','hope','world','life','a','real','the','you','yours','reality','magic']

# finding whether an element is present in list or not
'python' in set(my_list)
True

